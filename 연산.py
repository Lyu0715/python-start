# 사칙 연산자: +, -, *, /
# 거듭제곱 연산자: **
# 비교 연산자: <, >, <=, >=, ==

# a < b
# a가 가리키는 값이 b가 가리키는 값보다 작을 때 참
# a <= b
# a가 가리키는 값이 b가 가리키는 값보다 작거나 같을 때 참
# a > b
# a가 가리키는 값이 b가 가리키는 값보다 클 때 참
# a >= b
# a가 가리키는 값이 b가 가리키는 값보다 크거나 같을 때 참
# a == b
# a와 b가 동일한 값을 가리킬 때 참
# a != b
# a와 b가 동일하지 않은 값을 가리킬 때 참

# 리스트를 + 를 써서 이어붙이면 합쳐짐

